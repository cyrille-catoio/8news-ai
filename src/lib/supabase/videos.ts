/**
 * Look up the per-video SSR page row by its (topic, date, slug) tuple.
 *
 * Joins `video_transcriptions` with `youtube_videos` so the route gets
 * everything it needs in a single roundtrip: the AI summary + raw
 * transcript on one side, the YouTube metadata (thumbnail, duration,
 * channel title, link, view count) on the other.
 *
 * Lang is intentionally NOT a query filter: the slug is per-lang, so
 * a `(topic_id, published_date, slug_keywords)` triple matches at most
 * one row, and the lang is read from the returned row. See default #3
 * in the plan.
 *
 * Returns `null` when the route doesn't exist (slug typo, deleted row).
 */
export async function getVideoPageBySlug(
  topicId: string,
  date: string,
  slug: string,
): Promise<{
  id: number;
  video_id: string;
  channel_id: string;
  title: string;
  /** Per-lang translated title (NULL on legacy rows; fall back to `title`/youtube_videos.title). */
  title_localized: string | null;
  lang: string;
  summary_md: string;
  transcript: string;
  word_count: number | null;
  topic_id: string;
  published_date: string;
  slug_keywords: string;
  created_at: string;
  video: {
    title: string;
    description: string | null;
    channel_title: string;
    published: string;
    thumbnail: string | null;
    view_count: string | null;
    duration_sec: number | null;
    link: string;
  } | null;
} | null> {
  const clientP = getServerClient();
  if (!clientP) return null;
  try {
    const supabase = await clientP;
    // Try to read `title_localized` (migration 023). Fall back to the
    // pre-023 column list when the column is missing (env not yet
    // migrated) so the page keeps rendering with the YouTube title.
    const baseColumns =
      "id, video_id, channel_id, title, lang, summary_md, transcript, word_count, topic_id, published_date, slug_keywords, created_at";
    const fullColumns = `${baseColumns}, title_localized`;

    const run = (columns: string) =>
      supabase
        .from("video_transcriptions")
        .select(columns)
        .eq("topic_id", topicId)
        .eq("published_date", date)
        .eq("slug_keywords", slug)
        .limit(1)
        .maybeSingle();

    let res = await run(fullColumns);
    if (res.error && /title_localized/i.test(res.error.message ?? "")) {
      res = await run(baseColumns);
    }
    if (res.error || !res.data) return null;

    const row = res.data as unknown as {
      id: number; video_id: string; channel_id: string; title: string;
      title_localized?: string | null;
      lang: string; summary_md: string; transcript: string;
      word_count: number | null; topic_id: string; published_date: string;
      slug_keywords: string; created_at: string;
    };

    // Pull the YouTube cache row in a separate query — keeps the typing
    // straightforward and the join is anyway 1-1 by `video_id`.
    const { data: vidRow } = await supabase
      .from("youtube_videos")
      .select("title, description, channel_title, published, thumbnail, view_count, duration_sec, link")
      .eq("video_id", row.video_id)
      .limit(1)
      .maybeSingle();

    return {
      ...row,
      title_localized: row.title_localized ?? null,
      video: (vidRow as {
        title: string; description: string | null; channel_title: string;
        published: string; thumbnail: string | null; view_count: string | null;
        duration_sec: number | null; link: string;
      } | null) ?? null,
    };
  } catch {
    return null;
  }
}

/**
 * Latest N video pages for a given topic + lang. Used by the SSR
 * `/{topic}/v/{date}/{slug}` page for the "Sur le même sujet" sidebar.
 *
 * Filters by lang here (unlike the slug lookup) because we want all
 * featured items in the visitor's current language.
 *
 * Excludes the current video by `excludeVideoId` so we don't recommend
 * the page that's already being viewed.
 */
export async function getRecentVideoPagesForTopic(
  topicId: string,
  lang: string,
  limit: number,
  excludeVideoId?: string,
): Promise<Array<{
  video_id: string;
  title: string;
  /** Per-lang translated title (NULL on legacy rows). */
  title_localized: string | null;
  published_date: string;
  slug_keywords: string;
}>> {
  const clientP = getServerClient();
  if (!clientP) return [];
  try {
    const supabase = await clientP;
    const buildQuery = (columns: string) => {
      let q = supabase
        .from("video_transcriptions")
        .select(columns)
        .eq("topic_id", topicId)
        .eq("lang", lang)
        .not("slug_keywords", "is", null)
        .not("published_date", "is", null)
        .order("published_date", { ascending: false })
        .limit(limit);
      if (excludeVideoId) q = q.neq("video_id", excludeVideoId);
      return q;
    };

    const fullColumns = "video_id, title, title_localized, published_date, slug_keywords";
    const baseColumns = "video_id, title, published_date, slug_keywords";

    let res = await buildQuery(fullColumns);
    if (res.error && /title_localized/i.test(res.error.message ?? "")) {
      res = await buildQuery(baseColumns);
    }
    if (res.error || !res.data) return [];
    const rows = ((res.data ?? []) as Array<Partial<{
      video_id: string;
      title: string;
      title_localized: string | null;
      published_date: string;
      slug_keywords: string;
    }>>);
    return rows.map((r) => ({
      video_id: r.video_id ?? "",
      title: r.title ?? "",
      title_localized: r.title_localized ?? null,
      published_date: r.published_date ?? "",
      slug_keywords: r.slug_keywords ?? "",
    }));
  } catch {
    return [];
  }
}

/**
 * Pull every transcribed video for one (topic, date, lang) tuple — the
 * list backing `/[topic]/videos/[date]` (v2.7.0+, drill-down from the
 * unified /archives hub when a day shows « N transcribed videos »).
 *
 * Filtered the same way as `getRecentVideoPagesForTopic` (slug + date
 * present so every row resolves to a real `/v/[topic]/[date]/[slug]`
 * page) plus an exact `published_date = date` constraint. Returns
 * `summary_score` so the list view can surface the meter; `title` and
 * `title_localized` so the renderer can prefer the translated title.
 *
 * Same 42703 fallback as the recent-videos helper for environments
 * where mig 023 / 021 hasn't been applied yet.
 */
export async function getVideoPagesForTopicDate(
  topicId: string,
  date: string,
  lang: string,
): Promise<
  Array<{
    video_id: string;
    title: string;
    title_localized: string | null;
    published_date: string;
    slug_keywords: string;
    summary_score: number | null;
  }>
> {
  const clientP = getServerClient();
  if (!clientP) return [];
  try {
    const supabase = await clientP;
    const buildQuery = (columns: string) =>
      supabase
        .from("video_transcriptions")
        .select(columns)
        .eq("topic_id", topicId)
        .eq("lang", lang)
        .eq("published_date", date)
        .not("slug_keywords", "is", null)
        .order("created_at", { ascending: false });

    const fullColumns =
      "video_id, title, title_localized, published_date, slug_keywords, summary_score";
    const withoutTitle =
      "video_id, title, published_date, slug_keywords, summary_score";
    const minimal = "video_id, title, published_date, slug_keywords";

    let res = await buildQuery(fullColumns);
    if (res.error && /title_localized/i.test(res.error.message ?? "")) {
      res = await buildQuery(withoutTitle);
    }
    if (res.error && /summary_score/i.test(res.error.message ?? "")) {
      res = await buildQuery(minimal);
    }
    if (res.error || !res.data) return [];

    const rows = (res.data ?? []) as Array<
      Partial<{
        video_id: string;
        title: string;
        title_localized: string | null;
        published_date: string;
        slug_keywords: string;
        summary_score: number | null;
      }>
    >;
    return rows.map((r) => ({
      video_id: r.video_id ?? "",
      title: r.title ?? "",
      title_localized: r.title_localized ?? null,
      published_date: r.published_date ?? "",
      slug_keywords: r.slug_keywords ?? "",
      summary_score: normalizeVideoScore(r.summary_score),
    }));
  } catch {
    return [];
  }
}

